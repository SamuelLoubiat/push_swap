#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	neg;

	i = 0;
	neg = 1;
	while ((*s >= 9 && *s <= 13) || *s == 32)
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			neg = -1;
		s++;
	}
	while (*s && ft_isdigit(*s))
	{
		i *= 10;
		i += (*s % 48);
		s++;
	}
	return (neg * i);
}
